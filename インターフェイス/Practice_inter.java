/**
* Faceクラス
* インターフェースクラス
*/
public interface Face{
	/**
	* mouthメソッド
	* 抽象メソッド
	*/
	public abstract String mouth();
	/**
	* eyeメソッド
	* 抽象メソッド
	* @param number //値は数値
	*/
	public abstract int eye(int number);
	/**
	* noseメソッド
	* 抽象メソッド
	*/
	public abstract void nose();
}


